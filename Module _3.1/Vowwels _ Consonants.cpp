// Vowwels and Consonants Using Switchcase
#include<stdio.h>
main()
{
	int chr;

	printf("Enter a character: ");
	scanf("%c",&chr);
	switch (chr)
	
	{
		case 'a':			
			case 'e':
				case 'i':
					case 'o':
						case 'u':
							printf(" Vowels ");
							break;
							default:
								printf(" Consonants");
			
	}
}
